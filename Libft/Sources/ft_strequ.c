#include "../Includes/libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int a;

	if (s1 == NULL || s2 == NULL)
		return (0);
	else
	{
		a = 0;
		if (((s1[a] == '\0' && s2[a] != '\0')) ||
				((s2[a] == '\0') && (s1[a] != '\0')))
			return (0);
		while ((s1[a] == s2[a]) && (s1[a] != '\0') && (s2[a] != '\0'))
			a++;
		if ((s1[a] == '\0') && (s2[a] == '\0'))
			return (1);
		return (0);
	}
}
